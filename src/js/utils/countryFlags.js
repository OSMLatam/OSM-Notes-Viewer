/**
 * Get flag emoji for a country name
 * @param {string} countryName - Country name (in English preferred)
 * @returns {string} Flag emoji or empty string if not found
 */
export function getCountryFlag(countryName) {
  if (!countryName) return '';

  // Try exact match first
  if (countryFlagMap[countryName]) {
    return countryFlagMap[countryName];
  }

  // Try case-insensitive match
  const countryNameLower = countryName.toLowerCase();
  for (const [key, flag] of Object.entries(countryFlagMap)) {
    if (key.toLowerCase() === countryNameLower) {
      return flag;
    }
  }

  // Return empty string if no match found
  return '';
}

/**
 * Get flag emoji for a country object
 * @param {Object} country - Country object with country_name_en, country_name_es, or country_name
 * @returns {string} Flag emoji or empty string if not found
 */
export function getCountryFlagFromObject(country) {
  if (!country) return '';

  // Try country_name_en first
  if (country.country_name_en) {
    const flag = getCountryFlag(country.country_name_en);
    if (flag) return flag;
  }

  // Try country_name_es
  if (country.country_name_es) {
    const flag = getCountryFlag(country.country_name_es);
    if (flag) return flag;
  }

  // Try country_name
  if (country.country_name) {
    const flag = getCountryFlag(country.country_name);
    if (flag) return flag;
  }

  return '';
}
